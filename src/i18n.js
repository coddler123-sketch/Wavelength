let _lang = 'de';

export function setLang(l) {
  _lang = T[l] ? l : 'de';
  document.documentElement.lang = _lang;
}
export function getLang() {
  return _lang;
}

export function t(key, ...args) {
  const str = T[_lang]?.[key] ?? T.de[key] ?? key;
  return args.reduce((s, a, i) => s.replace(`{${i}}`, a), str);
}

